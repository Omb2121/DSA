// MAXIMUM SUBARRAY SUM - KADANE'S ALGORITHM - O(N)
#include <iostream>
#include <vector>
#include <climits>
using namespace std;
int main()
{
    int n = 5;
    int arr[5] = {-2, 1, -3, 4, -1};
    int maxSum = INT_MIN;
    int currSum = 0;
    for(int i=0;i<n;i++)
    {
        currSum += arr[i];
        if(currSum > maxSum){
            maxSum = currSum;
        }
        if(currSum < 0){
            currSum = 0;
        }

    }
    cout<<maxSum;
    return 0;
}